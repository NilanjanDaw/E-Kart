<h3>Q1. My Accounts and Orders</h3>
<ul>
<li>You can make your account on ekart.com by simply clicking on login page provided on page itself, if you not a registered user than you can signup up by simply completing details form. just a click away!</li>

<li>When your ordered is confirmed, an email will be sent to your email account about confirmation with a link to track your order. SMS subscription can be done to track your order.</li>

<li>Out of stock product can be ordered but availibility depends upon stock updates of third party. When product will be in stock your order will be forwarded to delivery department.</li>
</ul>
