<h2>Safe and Secure Shopping</h2><br />

Is it safe to use my credit/debit card on ekart?
Your online transaction on ekart is secure with the highest levels of transaction security currently available on the Internet. ekart uses 256-bit encryption technology to protect your card information while securely transmitting it to the respective banks for payment processing.
All credit card and debit card payments on ekart are processed through secure and trusted payment gateways managed by leading banks. Banks now use the 3D Secure password service for online transactions, providing an additional layer of security through identity verification.

Does ekart store my credit/debit card infomation?
ekart stores the first 6 and last 4 digits of your card number in a secure and encrypted manner. The first 6 digits (also known as the Bank Identification Number) are used to identify the bank name and country where your card was issued. The first 6 and last 4 digits are together used for fraud detection and prevention purposes.
Payment Options

What credit/debit cards are accepted on ekart?
We accept VISA, MasterCard, Maestro and American Express credit/debit cards.

Do you accept payment made by credit/debit cards issued in other countries?
Yes! We accept VISA, MasterCard, Maestro, American Express credit/debit cards issued by banks in India and in the following countries:
Australia, Austria, Belgium, Canada, Cyprus, Denmark, Finland, France, Germany, Ireland, Italy, Luxembourg, the Netherlands, New Zealand, Norway, Portugal, Singapore, Spain, Sweden, the UK and the US.
Please note that we do not accept internationally issued credit/debit cards for Flyte, Wallet and eGV payments/top-ups.

What other payment options are available on ekart?
Apart from Credit and Debit Cards, we accept payments by Internet Banking (covering 44 banks), Cash-on-Delivery, Equated Monthly Installments (EMI), E-Gift Vouchers and ekart Wallet.

